public class calculadoraFuncoes {

    double primeiroValor;
    double segundoValor;
    double resultado;
    
    double somar(double valor1, double valor2 ){

        primeiroValor = valor1;
        segundoValor = valor2;
        resultado = primeiroValor + segundoValor;
        return resultado;
    }
    double subtrair(double valor1, double valor2){
        primeiroValor = valor1;
        segundoValor = valor2;
        resultado = primeiroValor + segundoValor;
        return resultado;
    }
    double mutiplicar(double valor1, double valor2){
        primeiroValor = valor1;
        segundoValor = valor2;
        resultado = primeiroValor + segundoValor;
        return resultado; 
    }
    double dividir(double valor1, double valor2){
        primeiroValor = valor1;
        segundoValor = valor2;
        resultado = primeiroValor + segundoValor; 
        return resultado;
    }
}